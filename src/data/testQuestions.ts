// Helper function to get section by ID
export function getQuestionById(id: number) {
  if (id >= 1 && id <= 20) {
    return { section: 'listening', question: test1Questions.listening[id - 1] };
  } else if (id >= 21 && id <= 50) {
    return { section: 'grammar', question: test1Questions.grammar[id - 21] };
  } else if (id >= 51 && id <= 70) {
    return { section: 'vocabulary', question: test1Questions.vocabulary[id - 51] };
  } else if (id >= 71 && id <= 80) {
    return { section: 'reading', question: test1Questions.reading[id - 71] };
  }
  return null;
}

// Helper function to calculate score
export function calculateSectionScore(answers: number[], section: 'listening' | 'grammar' | 'vocabulary' | 'reading'): number {
  const questions = test1Questions[section];
  let correct = 0;
  
  answers.forEach((answer, index) => {
    if (questions[index] && answer === questions[index].correct) {
      correct++;
    }
  });
  
  return correct;
}
